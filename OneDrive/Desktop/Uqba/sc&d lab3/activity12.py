List=['g','e','e','k','s','f','o','r','g','e','e','k','s']
print("\ninitial list")
print(List)
sliced_list=List[:-6]
print("\nelements sliced till 6th element from last")
print(sliced_list)
sliced_list=List[-6:-1]
print("\nelements sliced from index -6 to -1")
print(sliced_list)
sliced_list=List[::-1]
print("\nprinting list in reverse")
print(sliced_list)
